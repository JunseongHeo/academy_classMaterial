package ch11;

public class Car {
	public String model;
	public String owner;
	
	public Car() {
	}
	public Car(String model) {
		this.model = model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getModel() {
		return model;
	}
	private void setOwner(String Owner) {
		this.owner = owner;
	}
	public String getOwner() {
		return owner;
	}
	
}
